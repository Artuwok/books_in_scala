package scala_for_impatient.chapter_06

// Write an object Conversions with methods inchesToCentimeters, gallonsToLiters, and milesToKilometers.
object Ex_01 {

  def inchesToCentimeters(inches: Double): Double = 2.54 * inches

  def gallonsToLitres(gallons: Double): Double = 3.78541 * gallons

  def milesToKilometers(miles: Double): Double = 1.60934 * miles

}
